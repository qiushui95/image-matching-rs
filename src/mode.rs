//! 匹配器模式定义
//!
//! 该枚举用于选择图像匹配的算法模式：
//! - `FFT`：基于快速傅里叶变换的归一化互相关（NCC）匹配，适合大图、需要高精度场景；
//! - `Segmented`：基于分段统计的匹配，适合内存受限或快速初筛场景。

/// 匹配器模式枚举
///
/// 用于在创建匹配器或执行匹配时指定算法模式。
#[derive(Debug, Clone, Copy)]
pub enum MatcherMode {
    /// FFT 模式
    ///
    /// 参数说明：
    /// - `src_width`：待匹配的源图像宽度
    /// - `src_height`：待匹配的源图像高度
    FFT { src_width: u32, src_height: u32 },
    /// 分段模式
    ///
    /// 不需要源图尺寸参数，通过模板分段的统计特征进行匹配。
    Segmented,
}
